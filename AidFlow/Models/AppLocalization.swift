import Foundation

enum AppLanguage: String, CaseIterable, Identifiable {
    case system
    case english
    case simplifiedChinese
    case traditionalChinese
    case arabic
    case vietnamese

    var id: String { rawValue }

    var title: String {
        switch self {
        case .system:
            return AppStrings.text("System")
        case .english:
            return "English"
        case .simplifiedChinese:
            return "简体中文（Beta）"
        case .traditionalChinese:
            return "廣東話（Beta）"
        case .arabic:
            return "العربية (Beta)"
        case .vietnamese:
            return "Tiếng Việt (Beta)"
        }
    }

    static var current: AppLanguage {
        let stored = UserDefaults.standard.string(forKey: "appLanguage") ?? AppLanguage.system.rawValue
        return AppLanguage(rawValue: stored) ?? .system
    }

    var resolvedLanguage: AppLanguage {
        switch self {
        case .system:
            let preferredLanguages = Locale.preferredLanguages.map { $0.lowercased() }
            if preferredLanguages.contains(where: { $0.hasPrefix("zh-hant") || $0.hasPrefix("zh-hk") || $0.hasPrefix("zh-tw") }) {
                return .traditionalChinese
            }
            if preferredLanguages.contains(where: { $0.hasPrefix("zh-hans") || $0.hasPrefix("zh-cn") }) {
                return .simplifiedChinese
            }
            if preferredLanguages.contains(where: { $0.hasPrefix("ar") }) {
                return .arabic
            }
            if preferredLanguages.contains(where: { $0.hasPrefix("vi") }) {
                return .vietnamese
            }
            return .english
        default:
            return self
        }
    }

    var usesRightToLeftLayout: Bool {
        resolvedLanguage == .arabic
    }

    var usesSimplifiedChinese: Bool {
        switch resolvedLanguage {
        case .simplifiedChinese:
            return true
        case .system, .english, .traditionalChinese, .arabic, .vietnamese:
            return false
        }
    }
}

enum AppStrings {
    static func text(_ key: String) -> String {
        switch AppLanguage.current.resolvedLanguage {
        case .simplifiedChinese:
            return simplifiedChinese[key] ?? key
        case .traditionalChinese:
            return traditionalChinese[key] ?? key
        case .arabic:
            return arabic[key] ?? key
        case .vietnamese:
            return vietnamese[key] ?? key
        case .system, .english:
            return key
        }
    }

    static func text(_ key: String, _ arguments: CVarArg...) -> String {
        String(format: text(key), arguments: arguments)
    }

    static func display(_ value: String) -> String {
        text(value)
